package org.profamilia.hc.model.dto;

public class Chunidades {

    private String hunccodigo;

    private String huncdescripcio;


    public Chunidades() {
    }


    public void setHunccodigo(String hunccodigo) {
        this.hunccodigo = hunccodigo;
    }

    public String getHunccodigo() {
        return hunccodigo;
    }

    public void setHuncdescripcio(String huncdescripcio) {
        this.huncdescripcio = huncdescripcio;
    }

    public String getHuncdescripcio() {
        return huncdescripcio;
    }
}
